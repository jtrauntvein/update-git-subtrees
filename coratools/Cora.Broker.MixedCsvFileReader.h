#pragma once
#ifndef Cora_Broker_MixedCsvFileReader_h
#define Cora_Broker_MixedCsvFileReader_h

#include "Cora.Broker.DataFileReader.h"
#include "Cora.Broker.FSLReader.h"


namespace Cora
{
   namespace Broker
   {
      ////////////////////////////////////////////////////////////
      // class MixedCsvFileReader
      //
      // Defines a class that parses a Mixed Csv file in terms of
      // Cora::Broker::record objects.
      ////////////////////////////////////////////////////////////
      class MixedCsvFileReader: public DataFileReader
      {
      public:
         ////////////////////////////////////////////////////////////
         // default constructor
         //
         // Creates the object in an unopened state
         ////////////////////////////////////////////////////////////
         MixedCsvFileReader(
            value_factory_handle value_factory = 0);

         ////////////////////////////////////////////////////////////
         // destructor
         ////////////////////////////////////////////////////////////
         virtual ~MixedCsvFileReader();

         ////////////////////////////////////////////////////////////
         // open
         //
         // Attempts to open the specified file and sets up the current
         // description based upon information obtained from the file header.
         // If another file is already opened, that file will be closed.
         ////////////////////////////////////////////////////////////
         virtual void open(
            StrAsc const &file_name, StrAsc const &labels_file_name);

         ////////////////////////////////////////////////////////////
         // close
         //
         // Closes the currently opened file and invalidates the current
         // description.
         ////////////////////////////////////////////////////////////
         virtual void close();

         ////////////////////////////////////////////////////////////
         // hibernate
         ////////////////////////////////////////////////////////////
         virtual void hibernate();

         ////////////////////////////////////////////////////////////
         // wake_up
         ////////////////////////////////////////////////////////////
         virtual bool wake_up(bool &all_data_overwritten);

         ////////////////////////////////////////////////////////////
         // read_next_record
         //
         // Attempts to read the next record from the currently opened file.
         // The application is responsible for providing a record object for
         // the data to be read into.  It can create these objects by calling
         // make_record().
         ////////////////////////////////////////////////////////////
         virtual read_outcome_type read_next_record(
            record_handle &destination,
            bool *file_mark_after_record = 0,
            bool *remove_mark_after_record = 0,
            uint4 array_id = 0);

         ////////////////////////////////////////////////////////////
         // generate_index
         ////////////////////////////////////////////////////////////
         virtual void generate_index(index_type &index, bool &should_abort, uint4 *next_record_no);

         ////////////////////////////////////////////////////////////
         // has_record_no
         ////////////////////////////////////////////////////////////
         virtual bool has_record_no() const
         { return false; }

         ////////////////////////////////////////////////////////////
         // get_header_len
         ////////////////////////////////////////////////////////////
         virtual int8 get_header_len()
         { return 0; }

         ////////////////////////////////////////////////////////////
         // get_header_sig
         ////////////////////////////////////////////////////////////
         virtual uint2 get_header_sig();

         ////////////////////////////////////////////////////////////
         // get_data_len
         ////////////////////////////////////////////////////////////
         virtual int8 get_data_len();

         ////////////////////////////////////////////////////////////
         // get_data_offset
         ////////////////////////////////////////////////////////////
         virtual int8 get_data_offset();

         ////////////////////////////////////////////////////////////
         // seek_data
         ////////////////////////////////////////////////////////////
         virtual void seek_data(int8 offset, bool search_for_prev = true);

         ///////////////////////////////////////////////////////////
         // seek_data_with_index
         ///////////////////////////////////////////////////////////
         void seek_data_with_index(int8 data_len, uint4 array_id);

         ////////////////////////////////////////////////////////////
         // seek_to_newest
         ////////////////////////////////////////////////////////////
         virtual void seek_to_newest(uint4 array_id)
         { seek_data_with_index(get_data_len(), array_id); }

         ///////////////////////////////////////////////////////////
         // set_fsl
         ///////////////////////////////////////////////////////////
         void set_fsl(StrAsc const &labels_file_name);
         
      private:
         ////////////////////////////////////////////////////////////
         // input
         //
         // If not null, refers to the current file that the record description
         // was generated from.
         ////////////////////////////////////////////////////////////
         FILE *input;

         ///////////////////////////////////////////////////////////
         // fsl_reader
         ///////////////////////////////////////////////////////////
         Csi::SharedPtr<FSLReader> fsl_reader;

         ////////////////////////////////////////////////////////////
         // file_name
         ////////////////////////////////////////////////////////////
         StrAsc file_name;

         ////////////////////////////////////////////////////////////
         // is_sleeping
         ////////////////////////////////////////////////////////////
         bool is_sleeping;

         ////////////////////////////////////////////////////////////
         // hibernate_pos
         ////////////////////////////////////////////////////////////
         int8 hibernate_pos;

         ////////////////////////////////////////////////////////////
         // hibernate_sig
         ////////////////////////////////////////////////////////////
         uint2 hibernate_sig;

         ////////////////////////////////////////////////////////////
         // hibernate_len
         ////////////////////////////////////////////////////////////
         int8 hibernate_len;
      };
   };
};


#endif
